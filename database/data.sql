-- * Automatic inventory (Crazy!)
INSERT INTO Inventory (identifier, quantity, rate)
SELECT 
    identifier,
    SUM(quantity) AS total_quantity,
    MAX(rate) * 1.4 AS rate
FROM Produced
GROUP BY identifier
ON DUPLICATE KEY UPDATE
    quantity = VALUES(quantity) + quantity;

